        System.out.println(RED_BACKGROUND + WHITE + "빨간색 배경에 흰색 텍스트");
        System.out.println(GREEN + UNDERLINE + "초록색 텍스트에 밑줄 효과");
        System.out.println(BLUE_BACKGROUND + YELLOW + "파란색 배경에 노란색 텍스트");
        System.out.println(PURPLE + ITALIC + "자주색 이탤릭체 텍스트");

        System.out.println(RESET + "초기화된 텍스트");
        
        System.out.print(ConsoleColors.BOLD_WHITE_BACKGROUND);
        System.out.print("Hello, World!");
        System.out.print(ConsoleColors.BOLD_UNDERLINE);
        System.out.println();
        System.out.println();
        System.out.print(BOLD_WHITE_BACKGROUND+BOLD_UNDERLINE+"Hello, World!");
